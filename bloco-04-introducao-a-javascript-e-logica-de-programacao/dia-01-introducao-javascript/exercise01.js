let operator = '+';
let a = 10;
let b = 2;

switch(operator){
    case '+':
        console.log(a + b);
        break;
    case '-':
        console.log(a - b);
        break;
    case '*':
        console.log(a * b);
        break;
    case '/':
        console.log(a / b);
        break;
    case '%':
        console.log(a % b);
        break;
}